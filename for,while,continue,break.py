# Break the for loop at 'blue'
colors = ['red','green','blue','yellow']
for x in colors:
  if x == 'blue':
    break
  print(x)

# Break the while loop when x becomes 3

x = int(input('Enter the number between 6 to 12: '))

while x:
  print(x)
  x -= 1
  if x == 3:
    break

